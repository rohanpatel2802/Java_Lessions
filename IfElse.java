package Java_Lessons;

public class IfElse {
    public static void main(String[] args) {
        //Code for if-else statement
        int a=3;
        if(a%2==0){
            System.out.println(a+" is an even number");
        }
        else{
            System.out.println(a+" is an odd number");
        }
        
    }
    
}
